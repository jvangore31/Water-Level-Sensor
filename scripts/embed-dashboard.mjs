import { execFileSync } from 'node:child_process'
import { gzipSync } from 'node:zlib'
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const npm = process.platform === 'win32' ? 'npm.cmd' : 'npm'

execFileSync(npm, ['run', 'build'], { cwd: join(root, 'dashboard'), stdio: 'inherit' })

const dist = join(root, 'dashboard', 'dist')
let html = readFileSync(join(dist, 'index.html'), 'utf8')

html = html.replace(/<link rel="stylesheet"[^>]+href="([^"]+)"[^>]*>/, (_tag, href) => {
  return `<style>${readFileSync(join(dist, href.replace(/^\//, '')), 'utf8')}</style>`
})

html = html.replace(/<script type="module"[^>]+src="([^"]+)"[^>]*><\/script>/, (_tag, src) => {
  return `<script type="module">${readFileSync(join(dist, src.replace(/^\//, '')), 'utf8')}</script>`
})

const compressed = gzipSync(Buffer.from(html), { level: 9 })
const rows = []
for (let index = 0; index < compressed.length; index += 16) {
  rows.push(`  ${[...compressed.subarray(index, index + 16)].map((byte) => `0x${byte.toString(16).padStart(2, '0')}`).join(', ')}`)
}

const header = `#pragma once
#include <Arduino.h>

// Generated by npm run build:firmware-assets. Do not edit by hand.
const uint8_t DASHBOARD_HTML_GZ[] PROGMEM = {
${rows.join(',\n')}
};
const size_t DASHBOARD_HTML_GZ_LEN = sizeof(DASHBOARD_HTML_GZ);
`

const destination = join(root, 'firmware', 'water_level_sensor', 'dashboard_assets.h')
writeFileSync(destination, header)
console.log(`Embedded dashboard: ${html.length} bytes HTML, ${compressed.length} bytes gzip`)
